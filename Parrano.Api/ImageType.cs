namespace Parrano.Api
{
    /// <summary>
    /// The possible image types handled by PSLib
    /// </summary>
    public enum ImageType
    {
        /// <summary>
        /// PNG (Portable Network Graphic) image
        /// </summary>
        png,
        /// <summary>
        /// JPEG (Joint Photographic Experts Group) image
        /// </summary>
        jpeg,
        /// <summary>
        /// EPS (Encapsulated Postscript) image
        /// </summary>
        eps,
        /// <summary>
        /// GIF (Graphics Interchange Format) image
        /// </summary>
        gif,
        /// <summary>
        /// TIFF (Tagged Image File Format) image
        /// </summary>
        tiff,
        /// <summary>
        /// BMP (Bitmap) image
        /// </summary>
        bmp
    }
}